package edu.estudos.java;

public class Bitwise {

	public static void main(String[] args) {
		/*
		 * utilização de operadores bitwise // operadores bit a bit &(E), |(OU), ^(OU
		 * exclusivo) // utilizado pra saber o numero binario // muito utilizado em
		 * programacao de baixo nivel, microcontroladores, arduino
		 */

		// exemplo de codigo com bitwise

		int x = 89;
		int y = 60;

		System.out.println(x & y);
		System.out.println(x | y);
		System.out.println(x ^ y);
	}

}
